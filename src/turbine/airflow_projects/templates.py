PROJECT_START_SCRIPT_TEMPLATE = """#!/bin/bash
set -e

PROJECT_NAME={tgt_folder}
POSTGRES_VERSION=13
POSTGRES_CONTAINER="${{PROJECT_NAME}}-postgres-1"
AIRFLOW_WEBSERVER_CONTAINER="${{PROJECT_NAME}}-airflow-webserver-1"

# Check if containers are running
if [ "$(docker ps -q -f name=$POSTGRES_CONTAINER)" ]; then
    echo "Containers are already running. Stopping them..."
    docker-compose down
fi

# Start the services
echo "Starting Airflow services..."
docker-compose up -d

# Wait for the PostgreSQL container to be ready
echo "Waiting for PostgreSQL to be ready..."
until docker exec $POSTGRES_CONTAINER pg_isready -U airflow; do
    echo "PostgreSQL is unavailable - sleeping"
    sleep 1
done

echo "PostgreSQL is ready."

# Initialize the database
echo "Initializing Airflow database..."
docker-compose run --rm airflow-cli db init

# Create admin user
echo "Creating admin user..."
docker-compose run --rm airflow-cli users create \\
    --username admin \\
    --password admin \\
    --firstname FIRST_NAME \\
    --lastname LAST_NAME \\
    --role Admin \\
    --email admin@example.org

# Wait for the Airflow webserver to be ready
echo "Waiting for Airflow webserver to be ready..."
until curl -s -f -o /dev/null "http://localhost:{webserver_port}/health"; do
    echo "Airflow webserver is unavailable - sleeping"
    sleep 5
done

echo "Airflow is ready!"
echo "You can now access the Airflow web UI at: http://localhost:{webserver_port}"
echo "Login with username: admin, password: admin"
"""

